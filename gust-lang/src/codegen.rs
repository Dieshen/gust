/// Code generator: emits Rust source code from a Gust AST.
///
/// Each `machine` becomes:
///   - A state enum with serde derives
///   - A machine struct holding the current state
///   - An impl block with transition methods (type-checked)
///   - Transition methods that enforce valid from-state at runtime
///     (compile-time via enum matching / exhaustiveness)
///   - Effect traits (if machine declares effects)

use crate::ast::*;

pub struct RustCodegen {
    output: String,
    indent: usize,
}

impl RustCodegen {
    pub fn new() -> Self {
        Self {
            output: String::new(),
            indent: 0,
        }
    }

    pub fn generate(mut self, program: &Program) -> String {
        self.emit_prelude();

        // Emit type declarations as Rust structs
        for type_decl in &program.types {
            self.emit_type_decl(type_decl);
            self.newline();
        }

        // Emit each machine
        for machine in &program.machines {
            self.emit_machine(machine);
            self.newline();
        }

        self.output
    }

    fn emit_prelude(&mut self) {
        self.line("// Generated by Gust compiler — do not edit manually");
        self.line("use serde::{Serialize, Deserialize};");
        self.line("use gust_runtime::prelude::*;");
        self.newline();
    }

    // === Type Declarations ===

    fn emit_type_decl(&mut self, decl: &TypeDecl) {
        self.line("#[derive(Debug, Clone, Serialize, Deserialize)]");
        self.line(&format!("pub struct {} {{", decl.name));
        self.indent += 1;
        for field in &decl.fields {
            self.line(&format!(
                "pub {}: {},",
                field.name,
                self.type_expr_to_rust(&field.ty)
            ));
        }
        self.indent -= 1;
        self.line("}");
    }

    // === Machine ===

    fn emit_machine(&mut self, machine: &MachineDecl) {
        let name = &machine.name;
        let state_enum = format!("{name}State");

        // State enum
        self.emit_state_enum(name, &machine.states);
        self.newline();

        // Effect trait (if machine has effects)
        if !machine.effects.is_empty() {
            self.emit_effect_trait(machine);
            self.newline();
        }

        // Machine struct
        self.line("#[derive(Debug, Clone, Serialize, Deserialize)]");
        self.line(&format!("pub struct {name} {{"));
        self.indent += 1;
        self.line(&format!("pub state: {state_enum},"));
        self.indent -= 1;
        self.line("}");
        self.newline();

        // Transition error type
        self.line(&format!(
            r#"#[derive(Debug, Clone, thiserror::Error)]
pub enum {name}Error {{
    #[error("invalid transition '{{transition}}' from state '{{from}}'")]
    InvalidTransition {{ transition: String, from: String }},
    #[error("transition failed: {{reason}}")]
    Failed {{ reason: String }},
}}"#
        ));
        self.newline();

        // Impl block with transitions
        self.line(&format!("impl {name} {{"));
        self.indent += 1;

        // Constructor
        if let Some(first_state) = machine.states.first() {
            self.emit_constructor(name, &state_enum, first_state);
            self.newline();
        }

        // Current state helper
        self.line(&format!("pub fn state(&self) -> &{state_enum} {{"));
        self.indent += 1;
        self.line("&self.state");
        self.indent -= 1;
        self.line("}");
        self.newline();

        // Transition methods
        for transition in &machine.transitions {
            self.emit_transition_method(
                name,
                &state_enum,
                transition,
                &machine.handlers,
                &machine.states,
                &machine.effects,
            );
            self.newline();
        }

        self.indent -= 1;
        self.line("}");
    }

    fn emit_state_enum(&mut self, machine_name: &str, states: &[StateDecl]) {
        let enum_name = format!("{machine_name}State");
        self.line("#[derive(Debug, Clone, Serialize, Deserialize)]");
        self.line(&format!("pub enum {enum_name} {{"));
        self.indent += 1;
        for state in states {
            if state.fields.is_empty() {
                self.line(&format!("{},", state.name));
            } else {
                self.line(&format!("{} {{", state.name));
                self.indent += 1;
                for field in &state.fields {
                    self.line(&format!(
                        "{}: {},",
                        field.name,
                        self.type_expr_to_rust(&field.ty)
                    ));
                }
                self.indent -= 1;
                self.line("},");
            }
        }
        self.indent -= 1;
        self.line("}");
    }

    fn emit_effect_trait(&mut self, machine: &MachineDecl) {
        let trait_name = format!("{}Effects", machine.name);
        self.line(&format!("pub trait {trait_name} {{"));
        self.indent += 1;

        for effect in &machine.effects {
            let params: Vec<String> = effect
                .params
                .iter()
                .map(|p| format!("{}: &{}", p.name, self.type_expr_to_rust(&p.ty)))
                .collect();
            let return_type = self.type_expr_to_rust(&effect.return_type);
            let all_params = if params.is_empty() {
                "&self".to_string()
            } else {
                format!("&self, {}", params.join(", "))
            };
            self.line(&format!(
                "fn {}({}) -> {};",
                effect.name,
                all_params,
                return_type
            ));
        }

        self.indent -= 1;
        self.line("}");
    }

    fn emit_constructor(
        &mut self,
        _machine_name: &str,
        state_enum: &str,
        first_state: &StateDecl,
    ) {
        if first_state.fields.is_empty() {
            self.line("pub fn new() -> Self {");
            self.indent += 1;
            self.line(&format!(
                "Self {{ state: {state_enum}::{} }}",
                first_state.name
            ));
            self.indent -= 1;
            self.line("}");
        } else {
            // Constructor takes initial state fields as params
            let params: Vec<String> = first_state
                .fields
                .iter()
                .map(|f| format!("{}: {}", f.name, self.type_expr_to_rust(&f.ty)))
                .collect();
            self.line(&format!("pub fn new({}) -> Self {{", params.join(", ")));
            self.indent += 1;
            let field_names: Vec<&str> = first_state.fields.iter().map(|f| f.name.as_str()).collect();
            self.line(&format!(
                "Self {{ state: {state_enum}::{} {{ {} }} }}",
                first_state.name,
                field_names.join(", ")
            ));
            self.indent -= 1;
            self.line("}");
        }
    }

    fn emit_transition_method(
        &mut self,
        machine_name: &str,
        state_enum: &str,
        transition: &TransitionDecl,
        handlers: &[OnHandler],
        states: &[StateDecl],
        effects: &[EffectDecl],
    ) {
        let error_type = format!("{machine_name}Error");

        // Find matching handler for this transition
        let handler = handlers
            .iter()
            .find(|h| h.transition_name == transition.name);

        // Collect handler params (excluding self/state params)
        let extra_params = handler
            .map(|h| {
                h.params
                    .iter()
                    .map(|p| format!("{}: {}", p.name, self.type_expr_to_rust(&p.ty)))
                    .collect::<Vec<_>>()
                    .join(", ")
            })
            .unwrap_or_default();

        // Add effects parameter if machine has effects
        let has_effects = !effects.is_empty();
        let params_str = if has_effects {
            let effects_param = format!("effects: &impl {}Effects", machine_name);
            if extra_params.is_empty() {
                format!("&mut self, {effects_param}")
            } else {
                format!("&mut self, {extra_params}, {effects_param}")
            }
        } else {
            if extra_params.is_empty() {
                String::from("&mut self")
            } else {
                format!("&mut self, {extra_params}")
            }
        };

        self.line(&format!(
            "pub fn {}({params_str}) -> Result<(), {error_type}> {{",
            transition.name
        ));
        self.indent += 1;

        // Match on current state — only allow transition from the declared `from` state
        self.line("match &self.state {");
        self.indent += 1;

        // Look up the from-state to see if it has fields
        let from_state = states.iter().find(|s| s.name == transition.from);

        // Valid from-state arm with destructuring
        if let Some(state) = from_state {
            if state.fields.is_empty() {
                self.line(&format!("{state_enum}::{} => {{", transition.from));
            } else {
                let field_names: Vec<&str> = state.fields.iter().map(|f| f.name.as_str()).collect();
                self.line(&format!(
                    "{state_enum}::{} {{ {} }} => {{",
                    transition.from,
                    field_names.join(", ")
                ));
            }
        } else {
            // Fallback if state not found (shouldn't happen in valid AST)
            self.line(&format!("{state_enum}::{} => {{", transition.from));
        }

        self.indent += 1;

        // Emit handler body if present
        if let Some(handler) = handler {
            self.emit_block(&handler.body, state_enum, states);
        } else {
            // Default: transition to first target if no handler
            if let Some(first_target) = transition.targets.first() {
                // Look up target state to see if it needs field initialization
                let target_state = states.iter().find(|s| &s.name == first_target);
                if let Some(target) = target_state {
                    if target.fields.is_empty() {
                        self.line(&format!("self.state = {state_enum}::{first_target};"));
                    } else {
                        // Can't auto-initialize fields without arguments
                        self.line(&format!(
                            "// Cannot auto-transition to {first_target} - requires fields"
                        ));
                    }
                }
            }
        }

        self.line("Ok(())");
        self.indent -= 1;
        self.line("}");

        // All other states — invalid transition
        self.line(&format!(
            "_ => Err({error_type}::InvalidTransition {{",
        ));
        self.indent += 1;
        self.line(&format!(
            "transition: \"{}\".to_string(),",
            transition.name
        ));
        self.line("from: format!(\"{:?}\", self.state),");
        self.indent -= 1;
        self.line("}),");

        self.indent -= 1;
        self.line("}");

        self.indent -= 1;
        self.line("}");
    }

    // === Statement & Expression Codegen ===

    fn emit_block(&mut self, block: &Block, state_enum: &str, states: &[StateDecl]) {
        for stmt in &block.statements {
            self.emit_statement(stmt, state_enum, states);
        }
    }

    fn emit_statement(&mut self, stmt: &Statement, state_enum: &str, states: &[StateDecl]) {
        match stmt {
            Statement::Let { name, ty, value } => {
                if let Some(type_expr) = ty {
                    self.line(&format!(
                        "let {}: {} = {};",
                        name,
                        self.type_expr_to_rust(type_expr),
                        self.expr_to_rust(value)
                    ));
                } else {
                    self.line(&format!("let {} = {};", name, self.expr_to_rust(value)));
                }
            }
            Statement::Return(expr) => {
                self.line(&format!("return {};", self.expr_to_rust(expr)));
            }
            Statement::If {
                condition,
                then_block,
                else_block,
            } => {
                self.line(&format!("if {} {{", self.expr_to_rust(condition)));
                self.indent += 1;
                self.emit_block(then_block, state_enum, states);
                self.indent -= 1;
                if let Some(else_blk) = else_block {
                    self.line("} else {");
                    self.indent += 1;
                    self.emit_block(else_blk, state_enum, states);
                    self.indent -= 1;
                }
                self.line("}");
            }
            Statement::Goto { state, args } => {
                // Look up target state to determine if it has fields
                let target_state = states.iter().find(|s| &s.name == state);
                if let Some(target) = target_state {
                    if target.fields.is_empty() {
                        self.line(&format!("self.state = {state_enum}::{state};"));
                    } else {
                        // Map args to fields in order
                        let field_inits: Vec<String> = target
                            .fields
                            .iter()
                            .zip(args.iter())
                            .map(|(field, arg)| {
                                format!("{}: {}", field.name, self.expr_to_rust(arg))
                            })
                            .collect();
                        self.line(&format!(
                            "self.state = {state_enum}::{state} {{ {} }};",
                            field_inits.join(", ")
                        ));
                    }
                } else {
                    // Fallback if state not found
                    self.line(&format!("self.state = {state_enum}::{state};"));
                }
            }
            Statement::Perform { effect, args } => {
                let arg_strs: Vec<String> = args.iter().map(|a| self.expr_to_rust(a)).collect();
                self.line(&format!("effects.{}({});", effect, arg_strs.join(", ")));
            }
            Statement::Expr(expr) => {
                self.line(&format!("{};", self.expr_to_rust(expr)));
            }
        }
    }

    fn expr_to_rust(&self, expr: &Expr) -> String {
        match expr {
            Expr::IntLit(v) => format!("{v}"),
            Expr::FloatLit(v) => format!("{v}"),
            Expr::StringLit(s) => format!("\"{s}\".to_string()"),
            Expr::BoolLit(b) => if *b { "true" } else { "false" }.to_string(),
            Expr::Ident(name) => name.clone(),
            Expr::FieldAccess(base, field) => {
                format!("{}.{}", self.expr_to_rust(base), field)
            }
            Expr::FnCall(name, args) => {
                let arg_strs: Vec<String> = args.iter().map(|a| self.expr_to_rust(a)).collect();
                format!("{}({})", name, arg_strs.join(", "))
            }
            Expr::BinOp(left, op, right) => {
                format!(
                    "({} {} {})",
                    self.expr_to_rust(left),
                    self.binop_to_rust(op),
                    self.expr_to_rust(right)
                )
            }
            Expr::UnaryOp(op, expr) => {
                format!("({}{})", self.unaryop_to_rust(op), self.expr_to_rust(expr))
            }
            Expr::Perform(effect, args) => {
                let arg_strs: Vec<String> = args.iter().map(|a| self.expr_to_rust(a)).collect();
                format!("effects.{}({})", effect, arg_strs.join(", "))
            }
        }
    }

    fn binop_to_rust(&self, op: &BinOp) -> &'static str {
        match op {
            BinOp::Add => "+",
            BinOp::Sub => "-",
            BinOp::Mul => "*",
            BinOp::Div => "/",
            BinOp::Mod => "%",
            BinOp::Eq => "==",
            BinOp::Neq => "!=",
            BinOp::Lt => "<",
            BinOp::Lte => "<=",
            BinOp::Gt => ">",
            BinOp::Gte => ">=",
            BinOp::And => "&&",
            BinOp::Or => "||",
        }
    }

    fn unaryop_to_rust(&self, op: &UnaryOp) -> &'static str {
        match op {
            UnaryOp::Not => "!",
            UnaryOp::Neg => "-",
        }
    }

    // === Helpers ===

    fn type_expr_to_rust(&self, ty: &TypeExpr) -> String {
        match ty {
            TypeExpr::Simple(name) => map_type_name(name),
            TypeExpr::Generic(name, args) => {
                let mapped = map_type_name(name);
                let arg_strs: Vec<String> = args.iter().map(|a| self.type_expr_to_rust(a)).collect();
                format!("{mapped}<{}>", arg_strs.join(", "))
            }
        }
    }

    fn line(&mut self, text: &str) {
        let indent = "    ".repeat(self.indent);
        self.output.push_str(&indent);
        self.output.push_str(text);
        self.output.push('\n');
    }

    fn newline(&mut self) {
        self.output.push('\n');
    }
}

/// Map Gust type names to Rust type names
fn map_type_name(name: &str) -> String {
    match name {
        "String" => "String".to_string(),
        "i64" | "i32" | "u64" | "u32" | "f64" | "f32" | "bool" => name.to_string(),
        "Option" => "Option".to_string(),
        "Vec" => "Vec".to_string(),
        "Result" => "Result".to_string(),
        other => other.to_string(), // User-defined types pass through
    }
}
